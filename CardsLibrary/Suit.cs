namespace CardsLibrary
{
    /// <summary>
    /// The Suits available for a card.
    /// </summary>
    public enum Suit
    {
        /// <summary>
        /// The Clubs suit is called:
        /// </summary>
        Clubs = 1,

        /// <summary>
        /// The Diamonds suit is called:
        /// </summary>
        Diamonds = 2,

        /// <summary>
        /// The Spades suit is called:
        /// </summary>
        Spades = 3,

        /// <summary>
        /// The Hearts suit is called:
        /// </summary>
        Hearts = 4,

        /// <summary>
        /// The Joker suit is called (needs the value to be of Joker as well):
        /// </summary>
        Null = 0
    }
}
