namespace CardsLibrary
{
    /// <summary>
    /// The face Values available for a card.
    /// </summary>
    public enum Value
    {
        /// <summary>
        /// The Ace card is called:
        /// </summary>
        Ace = 1,

        /// <summary>
        /// The Two card is called:
        /// </summary>
        Two = 2,

        /// <summary>
        /// The Three card is called:
        /// </summary>
        Three = 3,

        /// <summary>
        /// The Four card is called:
        /// </summary>
        Four = 4,

        /// <summary>
        /// The Five card is called:
        /// </summary>
        Five = 5,

        /// <summary>
        /// The Six card is called:
        /// </summary>
        Six = 6,

        /// <summary>
        /// The Seven card is called:
        /// </summary>
        Seven = 7,

        /// <summary>
        /// The Eight card is called:
        /// </summary>
        Eight = 8,

        /// <summary>
        /// The Nice card is called:
        /// </summary>
        Nine = 9,

        /// <summary>
        /// The Ten card is called:
        /// </summary>
        Ten = 10,

        /// <summary>
        /// The Jack card is called:
        /// </summary>
        Jack = 11,

        /// <summary>
        /// The Queen card is called:
        /// </summary>
        Queen = 12,

        /// <summary>
        /// The King card is called:
        /// </summary>
        King = 13,

        /// <summary>
        /// The Joker card is called (needs suit to be of Joker as well):
        /// </summary>
        Null = 0
    }
}